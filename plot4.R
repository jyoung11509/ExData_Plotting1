data <- read.table("household_power_consumption.txt", sep=";", header=T, skip=66636, nrows=2879, col.names=c("Date","Time","Global_active_power","Global_reactive_power", "Voltage","Global_intensity","Sub_metering_1","Sub_metering_2","Sub_metering_3"))
data$datetime <- paste(data$Date, data$Time)
data$datetime <- strptime(data$datetime, format="%d/%m/%Y %H:%M:%S")
png(file="plot4.png")
par(mfrow=c(2,2))
plot(data$datetime,data$Global_active_power,type="l",ylab="Global Active Power (kilowatts)",xlab="")
with(data,plot(datetime,Voltage,type="l"))
with(data,plot(datetime,Sub_metering_1,type="l",ylab="Energy sub metering",xlab=""))
lines(data$datetime,data$Sub_metering_2,col="red")
lines(data$datetime,data$Sub_metering_3,col="blue")
legend("topright",legend=c("Sub_metering_1","Sub_metering_2","Sub_metering_3"),lty=c(1,1,1),col=c("black","red","blue"))
with(data,plot(datetime,Global_reactive_power,type="l"))
dev.off()
